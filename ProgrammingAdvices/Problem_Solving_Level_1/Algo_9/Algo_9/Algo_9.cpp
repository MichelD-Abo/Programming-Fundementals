#include <iostream>
using namespace std;

int main()
{
	short A, B, C;

	cout << "Enter first number A: \n";
	cin >> A;
	cout << endl;

	cout << "Enter first number B: \n";
	cin >> B;
	cout << endl;

	cout << "Enter first number C: \n";
	cin >> C;
	cout << endl;

	short sum = A + B + C;

	cout << A << " + " << B << " + " << C << " = " << sum;
}
